const month = "mar"

switch (month) {
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("February")
        break;
    case "mar":
        console.log("March")
        break;
    case 4:
        console.log("April")
        break;

    default:
        console.log("Default case match")
        break;
}